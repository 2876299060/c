#include<stdio.h>
int main()
{
	int i=-20;
	unsigned int j=10;
	printf("%d\n",i+j);
	//以补码的形式储存，源码的形式输出
	//按照补码的形式进行运算，最后格式化为有符号整数
	return 0;
}
